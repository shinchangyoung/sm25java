package array;

public class Array1 {
    public static void main(String[] args) {
        // 정수형 1차원 배열 a를 길이 5로 선언 (모든 요소는 기본값 0으로 초기화됨)
        int a[] = new int[5];

        // 정수형 1차원 배열 b를 선언과 동시에 초기화 (값: 1, 2, 3, 4, 5)
        int b[] = {1, 2, 3, 4, 5};

        // 정수형 1차원 배열 c를 new 키워드를 이용해 선언과 초기화 (값: 1, 2, 3, 4, 5)
        int c[] = new int[]{1, 2, 3, 4, 5};

        // 정수형 2차원 배열 d를 3행 3열로 선언 (모든 요소는 기본값 0으로 초기화됨)
        int d[][] = new int[3][3];


        // 정수형 2차원 배열 e를 3행으로 선언하되, 각 행의 열 길이는 아직 지정하지 않음
        int e[][] = new int[3][];
        // 첫 번째 행(0번 인덱스)에 열 길이 3짜리 배열 할당
        e[0] = new int[3];  // 예: e[0][0], e[0][1], e[0][2]
        // 두 번째 행(1번 인덱스)에 열 길이 2짜리 배열 할당
        e[1] = new int[2];  // 예: e[1][0], e[1][1]
        // 세 번째 행(2번 인덱스)에 열 길이 1짜리 배열 할당
        e[2] = new int[1];  // 예: e[2][0]

        int f[][] = new int[][]{{1,2,3},{4,5,6,7}};
        int g[][] = {{1,2,3},{4,5,6,}};
        int h[][];
        h = new int[3][3];
        int i[][];
//        i = {{1,2,3},{2,4,5}};


    }
}
